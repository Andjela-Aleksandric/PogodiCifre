    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        brojPokusaja++;
        if (brojPokusaja > 5) {
            JOptionPane.showMessageDialog(this, "MAKSIMALNI BROJ POKUSAJA JE 5, IZGUBILI STE");
            KlijentskiZahtev kz = new KlijentskiZahtev(null, Operacije.NOVA_IGRA);
            Komunikacija.getKmk().posaljiZahtev(kz);
            Komunikacija.getKmk().prekini();
            this.dispose();
            return;
        }
        JTable tabela = (JTable) evt.getSource();
        Point point = evt.getPoint();
        int red = tabela.rowAtPoint(point);
        int kolona = tabela.columnAtPoint(point);
        if (red != -1 && kolona!= -1){
        SkriveniBroj sb = new SkriveniBroj(-1, red, kolona);
            KlijentskiZahtev kz = new KlijentskiZahtev(sb, Operacije.POGADJANJE_CIFRE);
            Komunikacija.getKmk().posaljiZahtev(kz);
            /////////////////////////////////////////////////////////////////////////
            ServerskiOdgovor so = (ServerskiOdgovor) Komunikacija.getKmk().primiOdgovor();
            SkriveniBroj sbOdg = (SkriveniBroj) so.getOdgovor();
            if (sbOdg == null) {
                JOptionPane.showMessageDialog(this, "Ne postoji broj na tom polju");
                return;
            } else {
                tabela.getModel().setValueAt(sbOdg.getVrednost(), red, kolona);
                brojPogodaka++;
                if (brojPogodaka == 1) {
                    cifra1 = sbOdg.getVrednost();
                } else if (brojPogodaka == 2) {
                    cifra2 = sbOdg.getVrednost();
                } else if (brojPogodaka == 3) {
                    cifra3 = sbOdg.getVrednost();
                }
                int najveci = Math.max(cifra1, Math.max(cifra2, cifra3));
                int najmanji = Math.min(cifra1, Math.min(cifra2, cifra3));
                int srednji = cifra1 + cifra2 + cifra3 - najveci - najmanji;
                sifra = najveci * 100 + srednji * 10 + najmanji;
                if (brojPogodaka == 3) {
                    JOptionPane.showMessageDialog(this, "POBEDILI STE, sifra je " + sifra);
                    this.dispose();
                    return;
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

