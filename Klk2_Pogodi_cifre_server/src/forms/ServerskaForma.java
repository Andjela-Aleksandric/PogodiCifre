    public void pokreniIgru() {
        refreshTable();
        jTable1.getModel().addTableModelListener(new TableModelListener() {
            @Override
            public void tableChanged(TableModelEvent e) {
                if (controller.getListaSb().size() >= 3) {
                    JOptionPane.showMessageDialog(null, "Uneli ste vec 3 broja");
                } else {
                    int red = e.getFirstRow();
                    int kolona = e.getColumn();
                    TableModel tm = (TableModel) e.getSource();
                    if (tm.getValueAt(red, kolona) != null) {
                        int vrednost = (int) tm.getValueAt(red, kolona);
                        SkriveniBroj sb = new SkriveniBroj(vrednost, red, kolona);
                        boolean postoji = controller.postojiBroj(sb);
                        if (!postoji) {
                            controller.dodajBroj(sb);
                            if (controller.getListaSb().size() == 3) {
                                jButtonPokreniIgru.setEnabled(true);
                            }
                        } else {
                            tm.setValueAt(null, red, kolona);
                            JOptionPane.showMessageDialog(null, "TAJ BROJ VEC POSTOJI");
                        }
                    }
                }
            }
        });
    }

